DROP TABLE ACTORS CASCADE CONSTRAINTS;
DROP TABLE MOVIES CASCADE CONSTRAINTS;
DROP TABLE MOVIE_TO_ACTOR CASCADE CONSTRAINTS;

CREATE TABLE ACTORS (
  actorid NUMBER(11) NOT NULL,
  name varchar2(50),
  total_gross FLOAT,
  top_movie varchar2(100),
  num_movies NUMBER(11),
  top_movie_gross FLOAT,
  CONSTRAINT PK1 PRIMARY KEY (actorid)
);

CREATE TABLE MOVIES (
  movieid NUMBER(11) NOT NULL,
  title varchar2(100),
  year NUMBER(11),
  rating varchar2(6),
  genre varchar2(20),
  revenue FLOAT,
  CONSTRAINT PK2 PRIMARY KEY (movieid));

CREATE TABLE MOVIE_TO_ACTOR (
  actorid NUMBER(11) NOT NULL,
  movieid NUMBER(11) NOT NULL,
  CONSTRAINT PK3 PRIMARY KEY (actorid,movieid),
  CONSTRAINT FK1 FOREIGN KEY (movieid) REFERENCES MOVIES(movieid),
  CONSTRAINT FK2 FOREIGN KEY (actorid) REFERENCES ACTORS(actorid)
);

